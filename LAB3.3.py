x = int(input("x = "))
R = int(input("R = "))
y = 0

if x <= -5:
    y = -3
elif -5 < x <= -R:
    y = -3 + (3 * (x - R) / (5 - R))
elif -R < x <= R:
    y = -((R ** 2 - x ** 2) ** 0.5)
elif R < x <= 8:
    y = R * (x - R) / (8 - R)
else:
    y = R

print("y = ", y)
